void Tester::test1(int aNumIter)
{
   LFBackoff tDelayA(gParms.mDelayA1,gParms.mDelayA2);
   LFBackoff tDelayB(gParms.mDelayB1,gParms.mDelayB2);

   for (int i = 0; i < aNumIter; i++)
   {
      mMarkerTest1.doStart();
      tDelayA.delay();
      mMarkerTest1.doStop();
      tDelayB.delay();

      mCount++;
   }
}

